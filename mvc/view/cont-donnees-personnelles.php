    <h2>1.2  UTILISATION DES COOKIES</h2>

    <p>Nous utilisons des cookies afin d’améliorer l’accès à notre site et d’identifier les visiteurs réguliers, ou encore pour analyser le trafic sur notre site web. En outre, nos cookies améliorent l’expérience d’utilisateur grâce au suivi et au ciblage de ses intérêts. Cependant, cette utilisation des cookies n’est en aucune façon liée à des informations personnelles identifiables sur notre site.
    Pour en savoir plus sur les cookies et les droits accordés aux utilisateurs :</p>
    <p><a href="http://www.cnil.fr/vos-droits/vos-traces/les-cookies/">http://www.cnil.fr/vos-droits/vos-traces/les-cookies/</a></p>
    <p>COMMENT GÉRER SES COOKIES</p>
    <p>Vous pouvez contrôler tous vos cookies librement depuis les paramètres de votre ordinateur. La gestion de vos cookies peut être différente en fonction du navigateur que vous utilisez. Nous vous expliquons, ci-dessous, comment y accéder.</p>
    <p><u>Si vous utilisez Internet explorer</u></p>
    <p>Dans le menu « Outils », sélectionnez « Options Internet »<br>
    Cliquez ensuite sur l’onglet « Confidentialité »<br>
    Cliquez sur le bouton « Avancé » pour faire apparaître la fenêtre « Paramètres de confidentialité avancés ».<br>
    Cochez ensuite la case « Ignorer la gestion automatique des cookies », puis sélectionnez « Refuser » et sauvegardez vos préférences en cliquant sur « OK ».<br>
    <u>Consulter l’aide sur le site de Microsoft</u><br></p>
    <p><u>Si vous utilisez Mozilla Firefox</u></p>
    <p>Dans le menu en haut de la page cliquez sur « Outils », puis « Options »<br>
    Sélectionnez l’onglet « Vie privée »<br>
    Paramétrer le menu « Règles de conservation », cliquez sur « utiliser les paramètres personnalisés pour l’historique »<br>
    Décochez la case « accepter les cookies tiers » et sauvegardez vos préférences en cliquant sur « OK ».<br>
    <u>Consulter l’aide sur le site de Mozilla</u><br></p>
