   <h2>1.1  CONSERVATION DE VOS DONNEES PERSONNELLES</h2>
    <p>Nous recueillons des informations lorsque vous vous complétez les formulaires de contact et d’investisseurs. Les informations recueillies incluent votre nom, votre adresse e-mail, votre numéro de téléphone, votre fonction et entité d’appartenance.
    Ces informations sont enregistrées dans un fichier informatisé détenu par la société Hydratis, pour la constitution d’un fichier contact et sont soumises aux dispositions du règlement européen sur la protection des données (cf. art 6.1.fr).</p>
    <p>Ce fichier permet :<br>
    <p>Répondre à vos demandes d’information et de devis</p>
    <p>D’adresser des mailings d’information sur la société Hydratis</p>
    <p>Les données collectées seront accessibles aux seuls destinataires suivants: salariés et sous-traitants de Hydratis et seront conservées pendant 3 ans.</p>
    <p>Vous pouvez accéder aux données vous concernant, les rectifier, demander leur effacement ou exercer votre droit à la limitation du traitement de vos données. Consultez le site cnil.fr pour plus d’informations sur vos droits.</p>
